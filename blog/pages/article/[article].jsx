export async function getServerSideProps({ params }) {
	const { data: article, error } = await supabase
		.from("articles")
		.select("*")
		.eq("id", params.article)
		.single();

	if (error) {
		throw new Error(error);
	}

	return {
		props: {
			article,
		},
	};
}
